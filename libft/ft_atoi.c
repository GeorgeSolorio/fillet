#include "libft.h"

int		ft_atoi(const char *str)
{
	int num;
	int i;
	int sign;

	i = 0;
	num = 0;
	sign = 1;
	while (ft_isspace(str[i]))
		i++;
	if (str[i] == '-')
		sign = -1;
	if (str[i] == '+' || str[i] == '-')
		i++;
	while (ft_isdigit(str[i]))
	{
		num = 10 * num + (str[i] - '0');
		i++;
	}
	return (sign * num);
}
