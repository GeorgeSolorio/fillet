#include "libft.h"

char	*ft_strcat(char *restrict dest, const char *restrict src)
{
	int k;
	int i;

	k = 0;
	i = ft_strlen(dest);
	while (src[k] != '\0')
	{
		dest[i + k] = src[k];
		k++;
	}
	dest[i + k] = '\0';
	return (dest);
}
